package com.vantty.treemap.color;

public class RGB {
    
    private int r;
    private int g;
    private int b;
    
    public RGB(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }
    
    public int r() {
        return r;
    }
    
    public int g() {
        return g;
    }
    
    public int b() {
        return b;
    }
    
}
